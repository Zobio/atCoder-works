ld f(ll x, ll y, ll z) {
	if(x == 100 || y == 100 || z == 100) return dp[x][y][z] = 0;
	if(dp[x][y][z] != 0) return dp[x][y][z];
	ld u = x + y + z;
	ld ret = 0;
	ret += x / u * (f(x + 1, y, z) + 1);
	ret += y / u * (f(x, y + 1, z) + 1);
	ret += z / u * (f(x, y, z + 1) + 1);
	return dp[x][y][z] = ret;
}

