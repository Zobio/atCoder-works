int main() {
	ull n; cin >> n;
	ull cur = 1, ans = 0;
	while(cur <= n) {
		ull t1 = min(cur * 10 - 1, n), t2 = min(cur * 10 - 1, n) + 1; //Σの上限
		ull n1 = t1 - cur + 1, n2 = t2 - cur + 1; //個数
		if(n1 % 2 == 0) n1 /= 2; else n2 /= 2;
		ans = (ans + (n1 % MOD) * (n2 % MOD)) % MOD;
		cur *= 10;
	}
	cout << ans << endl;
}